clc;
fm=200;
fc=2000;
m=0.5;
fs=10000;
t=0:(1/fs):((2/fm)-(1/fs));
f=(-511*fs/1024):(fs/1024):(512*fs/1024);
mt=cos(2*pi*fm*t);
mf=fft(mt,1024);
mmf=abs(mf);
f_m=[mmf(514:1024) mmf(1:513)];
subplot(3,1,1);
plot(f,f_m);
legend('line spectra of message signal');
xlabel('frequency');
ylabel('amplitude');
title('line spectra of message signal');
grid on;
ct=cos(2*pi*fc*t);
cf=fft(ct,1024);
ccf=abs(cf);
f_c=[ccf(514:1024) ccf(1:513)];
subplot(3,1,2);
plot(f,f_c);
legend('line spectra of carrier signal');
xlabel('frequency');
ylabel('amplitude');
title('line spectra of carrier signal');
grid on;
st=(1+m.*mt).*ct;
sf=fft(st,1024);
ssf=abs(sf);
f_s=[ssf(514:1024) ssf(1:513)];
subplot(3,1,3);
plot(f,f_s);
legend('line spectra of AM signal');
xlabel('frequency');
ylabel('amplitude');
title('line spectra of AM signal');
grid on;